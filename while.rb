# assigns x the value (variable) of 10
x = 10
# evaluates true until x is less than 0, thus once it reaches 0 and evaluates to false
# the code will end (break).
while x > 0
  # prints x (over and over again, until x evaluates to false)
  puts x
  # decreases (x = x-1) the value of x by one
  x -= 1
end
